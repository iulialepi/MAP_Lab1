package Projekt;

public class Plant{

    private String PlantName;
    private String PlantId;
    private String PlantCategory;

    public String getId() {return PlantId;}

    public void setId(String id) {this.PlantId = id;}

    public String getName() {return PlantName;}

    public void setName(String name) {this.PlantName = name;}

    public String getCategory() {return PlantCategory;}

    public void setCategory(String category) {this.PlantCategory = category;}
}
